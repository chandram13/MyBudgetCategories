package MyBudgetCategories

import "fmt"

func myHousing(monthlyRent,interest){
	monthlyCost = monthlyRent * interest
}

func myTransportation(personalVehicle,rideShare){
	totalTransportation = personalVehicle + rideShare
}

func foodCosts(groceries,takeout){
	monthlyFood = groceries + takeout
}

func utilities(water,electrical,cable,internet,heating,cooling){
	totalUtilities = water + electrical + cable + internet + heating + cooling
}

func insurance(life,vehicle,electronics,home){
	totalInsurance = life + vehicle + electronics + home
}

func healthcare(individual,family){
	totalHealth = individual + family
}

func debtPayments(loans, interest){
	totalDebt = loans * interest
}



